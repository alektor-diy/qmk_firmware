// --- ▼ 無操作でLEDを消す処理 ▼ ---
void matrix_scan_user(void) {
    if (rgblight_is_enabled() && !rgblight_is_sleeping) {
        if (timer_elapsed32(rgblight_sleep_timer) > RGBLIGHT_SLEEP_TIMEOUT) {
            rgblight_disable_noeeprom();  // LED消灯
            rgblight_is_sleeping = true;
        }
    }
}
// --- ▲ --------------------- ▲ ---

// --- ▼ 操作時にLEDを復帰させる処理 ▼ ---
bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (record->event.pressed) {
        rgblight_sleep_timer = timer_read32();  // タイマーリセット

        if (rgblight_is_sleeping) {
            rgblight_enable_noeeprom();         // LED再点灯
            layer_state_set_user(layer_state);  // 色復帰
            rgblight_is_sleeping = false;
        }
    }
    return true;
}
